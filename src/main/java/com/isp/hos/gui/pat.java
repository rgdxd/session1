    public void pats() {
        Hos h = new Hos();
        h.Session();
        DateFormat format = new java.text.SimpleDateFormat("yyyy-MM-dd");
        Vector<String> tableHeaders = new Vector<>();
        tableHeaders.add("Номер");
        tableHeaders.add("Имя");
        tableHeaders.add("Фамилия");
        tableHeaders.add("Отчество");
        tableHeaders.add("Номер паспорта");
        tableHeaders.add("Серия паспорта");
        tableHeaders.add("Дата рождения");
        tableHeaders.add("Пол");
        tableHeaders.add("Адрес");
        tableHeaders.add("Номер телефона");
        tableHeaders.add("Почта");
        tableHeaders.add("Номер мед. карты");
        tableHeaders.add("Номер страх. полиса");
        Vector tableData = new Vector();
        for (Patient pat : new Patient().getPatientList(h.em)) {
            Medcard mcard = pat.getMedcardId();
            Policy pol = pat.getPolicyId();
            Vector<Object> oneRow = new Vector<Object>();
            oneRow.add(pat.getId());
            oneRow.add(pat.getName());
            oneRow.add(pat.getSurname());
            oneRow.add(pat.getPatronymic());
            oneRow.add(pat.getPassportNumber());
            oneRow.add(pat.getPassportSeries());
            oneRow.add(format.format(pat.getDateOfBith()));
            oneRow.add(pat.getGender());
            oneRow.add(pat.getAddress());
            oneRow.add(pat.getPhonenumber());
            oneRow.add(pat.getEmail());
            oneRow.add((mcard.getNumber()));
            oneRow.add(pol.getNumber());
            tableData.add(oneRow);
        }
        Vector<Object> oneRow = new Vector<Object>();
        oneRow.add("");
        oneRow.add("");
        tableData.add(oneRow);
        jTable1.setModel(new DefaultTableModel(tableData, tableHeaders));
    }

