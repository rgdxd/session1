    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Hos h = new Hos();
            h.Session();
            Patient pat = new Patient();
            pat.setName(jTextField1.getText());
            pat.setSurname(jTextField2.getText());
            pat.setPatronymic(jTextField3.getText());
            pat.setPassportNumber(Integer.parseInt(jTextField4.getText()));
            pat.setPassportSeries(Integer.parseInt(jTextField5.getText()));
            DateFormat formatter = new java.text.SimpleDateFormat("yyyy-MM-dd");
            java.util.Date date;
            date = (java.util.Date) formatter.parse(jFormattedTextField1.getText());
            pat.setDateOfBith(date);
            pat.setGender((String) jComboBox1.getSelectedItem());
            pat.setAddress(jTextField6.getText());
            pat.setPhonenumber(jFormattedTextField2.getText());
            pat.setEmail(jTextField7.getText());
            String asd = (String) jComboBox2.getSelectedItem();
            String[] add = asd.split(" ");
            pat.setMedcardId(new Medcard(Integer.parseInt(add[0])));
            String dsa = (String) jComboBox3.getSelectedItem();
            String[] dda = dsa.split(" ");
            pat.setPolicyId(new Policy(Integer.parseInt(dda[0])));
            pat.addPatient(pat, h.em);
        } catch (ParseException ex) {
            Logger.getLogger(pat.class.getName()).log(Level.SEVERE, null, ex);
        }
        pats();
    }//GEN-LAST:event_jButton1ActionPerformed

