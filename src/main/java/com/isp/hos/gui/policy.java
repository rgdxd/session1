    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Hos h = new Hos();
            h.Session();
            Policy pol = new Policy();
            pol.setNumber(Integer.parseInt(jTextField1.getText()));
            DateFormat formatter = new java.text.SimpleDateFormat("yyyy-MM-dd");
            java.util.Date date;
            date = (java.util.Date) formatter.parse(jFormattedTextField1.getText());
            pol.setEndDate(date);
            pol.addPolicy(pol, h.em);
        } catch (ParseException ex) {
            Logger.getLogger(policy.class.getName()).log(Level.SEVERE, null, ex);
        }
        policys();
    }//GEN-LAST:event_jButton1ActionPerformed

