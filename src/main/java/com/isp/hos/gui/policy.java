    public void policys() {
        Hos h = new Hos();
        h.Session();
        DateFormat format = new java.text.SimpleDateFormat("yyyy-MM-dd");
        Vector<String> tableHeaders = new Vector<>();
        tableHeaders.add("Номер");
        tableHeaders.add("Номер страхового полиса");
        tableHeaders.add("Дата окончания действия");
        Vector tableData = new Vector();
        for (Policy pol : new Policy().getPolicyList(h.em)) {
            Vector<Object> oneRow = new Vector<Object>();
            oneRow.add(pol.getId());
            oneRow.add(pol.getNumber());
            oneRow.add(format.format(pol.getEndDate()));
            tableData.add(oneRow);
        }
        Vector<Object> oneRow = new Vector<Object>();
        oneRow.add("");
        oneRow.add("");
        tableData.add(oneRow);
        jTable1.setModel(new DefaultTableModel(tableData, tableHeaders));
    }

