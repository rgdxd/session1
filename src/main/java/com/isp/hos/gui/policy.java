    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Hos h = new Hos();
            h.Session();
            Policy pol = new Policy();
            DateFormat formatter = new java.text.SimpleDateFormat("yyyy-MM-dd");
            java.util.Date date;
            pol.setNumber(Integer.parseInt(jTextField1.getText()));
            date = (java.util.Date) formatter.parse(jFormattedTextField1.getText());
            pol.setEndDate(date);
            pol.setId(Integer.parseInt(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 0))));
            pol.upDatePolicy(pol, h.em);
        } catch (ParseException ex) {
            Logger.getLogger(medcards.class.getName()).log(Level.SEVERE, null, ex);
        }
        policys();
    }//GEN-LAST:event_jButton2ActionPerformed

